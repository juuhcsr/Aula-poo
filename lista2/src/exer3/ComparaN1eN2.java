package exer3;

public class ComparaN1eN2 {

	static void compara(int n1, int n2) {
		  if (n1 > n2) {
		    System.out.println(n1 + " é maior do que " + n2);
		  } else if (n1 < n2) {
		    System.out.println(n1 + " é menor do que " + n2);
		  } else {
		    System.out.println(n1 + " é igual a " + n2);
		  }
		}
	
	
}
